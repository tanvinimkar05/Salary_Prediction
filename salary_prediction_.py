"""### Consolidated Model Training and Evaluation

Let's consolidate the training and evaluation of all models into a single loop to compare their performance more easily.
"""

from sklearn.linear_model import LinearRegression
from sklearn.svm import SVR
from sklearn.neighbors import KNeighborsRegressor
from sklearn.tree import DecisionTreeRegressor
from sklearn.ensemble import RandomForestRegressor
from sklearn.preprocessing import StandardScaler
from sklearn.metrics import r2_score, mean_squared_error

# Initialize a list to store model results
model_results = []

# Define the models to be evaluated
models = {
    "Linear Regression": LinearRegression(),
    "SVR": SVR(kernel='linear', C=0.1), # Keep parameters consistent with previous SVR
    "KNN Regressor": KNeighborsRegressor(n_neighbors=5), # Keep parameters consistent with previous KNN
    "Decision Tree Regressor": DecisionTreeRegressor(random_state=42), # Keep parameters consistent
    "Random Forest Regressor": RandomForestRegressor(n_estimators=100, random_state=42, n_jobs=-1) # Keep parameters consistent
}

# Loop through each model, train, predict, and evaluate
for name, model in models.items():
    print(f"\n--- Training and evaluating {name} ---")

    # Handle scaling for SVR specifically, as other models don't necessarily benefit or require it
    if name == "SVR":
        scaler = StandardScaler()
        X_train_processed = scaler.fit_transform(X_train)
        X_test_processed = scaler.transform(X_test)
    else:
        X_train_processed = X_train
        X_test_processed = X_test

    # Train the model
    model.fit(X_train_processed, y_train)

    # Make predictions
    y_pred = model.predict(X_test_processed)

    # Evaluate the model
    r2 = r2_score(y_test, y_pred)
    mse = mean_squared_error(y_test, y_pred)

    print(f"R-squared: {r2:.4f}")
    print(f"Mean Squared Error: {mse:.2f}")

    # Store results
    model_results.append({
        "Model": name,
        "R-squared": r2,
        "Mean Squared Error": mse
    })

# Display all results in a DataFrame for easy comparison
results_df = pd.DataFrame(model_results)
print("\n--- All Model Performance Summary ---")
display(results_df.sort_values(by='R-squared', ascending=False))

